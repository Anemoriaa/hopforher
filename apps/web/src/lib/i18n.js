function resolveLanguageFromLocales(locales) {
  for (const locale of getPreferredLocales(locales)) {
    const normalized = String(locale || "").toLowerCase();

    if (dictionaries[normalized]) {
      return normalized;
    }

    const baseLanguage = normalized.split("-")[0];

    if (dictionaries[baseLanguage]) {
      return baseLanguage;
    }
  }

  return DEFAULT_LANGUAGE;
}

function interpolate(template, variables = {}) {
  return String(template).replace(/\{(\w+)\}/g, (_, key) => {
    const value = variables[key];
    return value === undefined || value === null ? "" : String(value);
  });
}

export function translateUi(key, locales, variables = {}) {
  const language = resolveLanguageFromLocales(locales);
  const dictionary = dictionaries[language] || dictionaries[DEFAULT_LANGUAGE];
  const fallbackDictionary = dictionaries[DEFAULT_LANGUAGE];
  const value = dictionary[key] ?? fallbackDictionary[key] ?? key;
  return interpolate(value, variables);
}

export function createI18n(localeProfileOrLocales) {
  const locales = Array.isArray(localeProfileOrLocales)
    ? getPreferredLocales(localeProfileOrLocales)
    : getPreferredLocales(localeProfileOrLocales?.locales);
  const language = resolveLanguageFromLocales(locales);

  return {
    language,
    locales,
    t(key, variables = {}) {
      return translateUi(key, locales, variables);
    },
  };
}
